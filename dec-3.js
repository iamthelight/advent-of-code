let countTrees = (path) => {
    if(path === '#'){
        return 1;
    } else {
        return 0;
    }
}

let checkSlopeMap = (slopeMap) => {
    let right = 0;
    let treeCount = 0;
    for(down = 0; down < slopeMap.length; down++){
        let level = slopeMap[down];
        console.log(`slopeMap[${down}] = ${level}`);
        console.log(`length: ${level.length}`);
        console.log(`right: ${right}`);

        if(right >= level.length){
            right = right - (level.length);
            console.log(`right: ${right}`);
        }
        console.log(`slopeMap[${down}].charAt(${right}) = ${level.charAt(right)}`);
        console.log(`Additional Tree: ${countTrees(level.charAt(right))}`);
        treeCount += countTrees(level.charAt(right));
        right += 3;
    }
    console.log(treeCount);
};

// checkSlopeMap(arr);

let checkDynamicSlopeMap = (slopeMap, d, r) => {
    let treeCount = 0;
    let right = 0;
    for(down = 0; down < slopeMap.length; down+= d){
        let level = slopeMap[down];

        if(right >= level.length){
            right = right - (level.length);
        }
        treeCount += countTrees(level.charAt(right));
        right += r;
    }
    return(treeCount);
};

